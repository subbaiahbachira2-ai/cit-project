public class IfElseIfLadder {
   
    public static void main(String[] args) {
        int marks=83;
        if(marks>=90){
            System.out.println("A grade");
        }else if(marks>80){
            System.out.println("B grade");
        }else if(marks>70){
            System.out.println("c grade");
        }else if(marks>60){
            System.out.println("D grade");
        }else
            System.out.println("Fail");
    

    }
}
